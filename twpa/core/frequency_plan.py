class ToneRole(str, Enum):
    """Semantic role of a frequency tone."""

    DC = "dc"
    PUMP = "pump"
    PUMP_HARMONIC = "pump_harmonic"
    SIGNAL = "signal"
    IDLER = "idler"
    SIDEBAND = "sideband"
    AUXILIARY = "auxiliary"


class FrequencyPlanKind(str, Enum):
    """Kind of frequency plan."""

    PUMP_ONLY = "pump_only"
    DP4WM_SIGNAL_IDLER = "dp4wm_signal_idler"
    NP4WM_SIGNAL_IDLER = "np4wm_signal_idler"
    FINITE_SIGNAL_HB = "finite_signal_hb"
    CUSTOM = "custom"


def _check_positive(name: str, value: float) -> None:
    if float(value) <= 0.0:
        raise ValueError(f"{name} must be positive, got {value!r}")


def _as_1d_float_array(name: str, value: ArrayLike) -> jax.Array:
    arr = jnp.asarray(value, dtype=jnp.float64)
    if arr.ndim != 1:
        raise ValueError(f"{name} must be a 1D array, got shape {arr.shape}")
    return arr


def _unique_labels(labels: Iterable[str]) -> list[str]:
    seen: set[str] = set()
    out: list[str] = []
    for label in labels:
        if label in seen:
            raise ValueError(f"Duplicate tone label {label!r}")
        seen.add(label)
        out.append(label)
    return out


def _jsonify(obj: Any) -> Any:
    if isinstance(obj, Enum):
        return obj.value
    if isinstance(obj, dict):
        return {str(k): _jsonify(v) for k, v in obj.items()}
    if isinstance(obj, (tuple, list)):
        return [_jsonify(v) for v in obj]
    if hasattr(obj, "shape") and hasattr(obj, "dtype"):
        return {
            "array_shape": tuple(int(s) for s in obj.shape),
            "array_dtype": str(obj.dtype),
        }
    return obj


def nearly_equal_frequency_mask(
    frequencies_hz: ArrayLike,
    target_hz: float,
    *,
    atol_hz: float = 1e-6,
    rtol: float = 1e-12,
) -> jax.Array:
    """
    Return boolean mask for frequencies approximately equal to target_hz.
    """
    f = jnp.asarray(frequencies_hz)
    return jnp.abs(f - target_hz) <= (atol_hz + rtol * jnp.abs(target_hz))


