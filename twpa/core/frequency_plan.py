def dp4wm_primary_idler_frequency_hz(
    pump_frequency_hz: float,
    signal_frequency_hz: float,
) -> float:
    """
    Primary idler frequency for degenerate-pump 4WM.

        f_i = 2 f_p - f_s
    """
    _check_positive("pump_frequency_hz", pump_frequency_hz)
    _check_positive("signal_frequency_hz", signal_frequency_hz)
    return 2.0 * pump_frequency_hz - signal_frequency_hz


def dp4wm_primary_idler_angular_frequency_rad_s(
    pump_omega_rad_s: float,
    signal_omega_rad_s: float,
) -> float:
    """
    Primary idler angular frequency for DP4WM.

        omega_i = 2 omega_p - omega_s
    """
    _check_positive("pump_omega_rad_s", pump_omega_rad_s)
    _check_positive("signal_omega_rad_s", signal_omega_rad_s)
    return 2.0 * pump_omega_rad_s - signal_omega_rad_s


def dp4wm_sideband_frequencies_hz(
    pump_frequency_hz: float,
    signal_frequency_hz: float,
    *,
    sideband_order: int,
) -> jax.Array:
    """
    Return f_k = f_s + 2 k f_p for k=-order..order.
    """
    _check_positive("pump_frequency_hz", pump_frequency_hz)
    _check_positive("signal_frequency_hz", signal_frequency_hz)
    if int(sideband_order) < 1:
        raise ValueError("sideband_order must be >= 1")
    k = jnp.arange(-int(sideband_order), int(sideband_order) + 1)
    return signal_frequency_hz + 2.0 * k * pump_frequency_hz


def dp4wm_sideband_indices(sideband_order: int) -> jax.Array:
    """Return integer k indices for f_k = f_s + 2 k f_p."""
    if int(sideband_order) < 1:
        raise ValueError("sideband_order must be >= 1")
    return jnp.arange(-int(sideband_order), int(sideband_order) + 1, dtype=jnp.int32)


def dp4wm_lattice_indices_for_plan(
    plan: FrequencyPlan,
    *,
    atol_hz: float = 1e-3,
    rtol: float = 1e-12,
) -> jax.Array:
    """
    Return the two-dimensional DP4WM lattice vector for every plan tone.

    The lattice fundamentals are ``(f_p, f_s)``. Pump harmonics use ``(h, 0)``
    and signal/idler sidebands ``f_s + 2 k f_p`` use ``(2 k, 1)``. Signed
    conjugates negate both coordinates.
    """
    if plan.reference_pump_hz is None or plan.reference_signal_hz is None:
        raise ValueError("DP4WM lattice projection requires reference pump and signal frequencies")
    fp = float(plan.reference_pump_hz)
    fs = float(plan.reference_signal_hz)

    def close(actual: float, expected: float) -> bool:
        return abs(actual - expected) <= atol_hz + rtol * max(abs(actual), abs(expected))

    rows: list[tuple[int, int]] = []
    for label, frequency in zip(plan.labels, plan.frequencies_hz.tolist()):
        f = float(frequency)
        if close(f, 0.0):
            rows.append((0, 0))
            continue

        harmonic = int(round(f / fp))
        if close(f, harmonic * fp):
            rows.append((harmonic, 0))
            continue

        sideband = int(round((f - fs) / (2.0 * fp)))
        if close(f, fs + 2.0 * sideband * fp):
            rows.append((2 * sideband, 1))
            continue

        negative_sideband = int(round((-f - fs) / (2.0 * fp)))
        if close(f, -(fs + 2.0 * negative_sideband * fp)):
            rows.append((-2 * negative_sideband, -1))
            continue

        raise ValueError(
            f"Tone {label!r} at {f} Hz is not on the DP4WM (pump, signal) lattice"
        )

    return jnp.asarray(rows, dtype=jnp.int32)


def is_valid_dp4wm_signal_band(
    pump_frequency_hz: float,
    signal_frequency_hz: ArrayLike,
    *,
    require_positive_idler: bool = True,
) -> jax.Array:
    """
    Return mask indicating whether signal frequencies give valid idlers.

    If require_positive_idler is true, require 2 fp - fs > 0.
    """
    fs = jnp.asarray(signal_frequency_hz)
    fi = 2.0 * pump_frequency_hz - fs
    if require_positive_idler:
        return fi > 0.0
    return jnp.ones_like(fs, dtype=bool)


