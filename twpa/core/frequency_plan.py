@dataclass(frozen=True)
class Tone:
    """
    One named frequency tone.

    Parameters
    ----------
    label:
        Unique label, e.g. "pump_1", "signal", "idler_primary".
    frequency_hz:
        Signed frequency in Hz. May be negative in double-sided plans.
    role:
        Semantic tone role.
    index:
        Integer sideband/harmonic index. Meaning depends on the plan.
    metadata:
        Small static metadata dictionary.
    """

    label: str
    frequency_hz: float
    role: ToneRole
    index: int = 0
    metadata: Mapping[str, Any] | None = None

    def __post_init__(self) -> None:
        object.__setattr__(self, "role", ToneRole(self.role))
        if self.frequency_hz == 0.0 and self.role != ToneRole.DC:
            raise ValueError("Only DC tones may have exactly zero frequency")
        object.__setattr__(self, "index", int(self.index))
        if self.metadata is None:
            object.__setattr__(self, "metadata", {})
        else:
            object.__setattr__(self, "metadata", dict(self.metadata))

    @property
    def angular_frequency_rad_s(self) -> float:
        return float(2.0 * jnp.pi * self.frequency_hz)

    @property
    def abs_frequency_hz(self) -> float:
        return abs(float(self.frequency_hz))

    @property
    def is_positive_frequency(self) -> bool:
        return self.frequency_hz > 0.0

    @property
    def is_negative_frequency(self) -> bool:
        return self.frequency_hz < 0.0

    def conjugate_label(self) -> str:
        """Suggested label for the conjugate/signed-opposite tone."""
        if self.label.startswith("neg_"):
            return self.label[4:]
        return "neg_" + self.label

    def conjugate(self) -> "Tone":
        """
        Return the signed-opposite tone.

        This is useful for double-sided bookkeeping. The role is preserved.
        """
        return Tone(
            label=self.conjugate_label(),
            frequency_hz=-self.frequency_hz,
            role=self.role,
            index=-self.index,
            metadata={**dict(self.metadata or {}), "conjugate_of": self.label},
        )

    def to_dict(self) -> dict[str, Any]:
        return {
            "label": self.label,
            "frequency_hz": self.frequency_hz,
            "frequency_GHz": self.frequency_hz / GHz,
            "angular_frequency_rad_s": self.angular_frequency_rad_s,
            "role": self.role.value,
            "index": self.index,
            "metadata": _jsonify(dict(self.metadata or {})),
        }


@jax.tree_util.register_pytree_node_class
@dataclass(frozen=True)
class FrequencyPlan:
    """
    Vectorized frequency plan used by HB and conversion calculations.

    Parameters
    ----------
    frequencies_hz:
        Signed ordinary frequencies in Hz, shape (K,).
    labels:
        Unique labels, length K.
    roles:
        Tone roles, length K.
    indices:
        Integer harmonic/sideband indices, length K.
    kind:
        Plan kind.
    reference_pump_hz:
        Main pump frequency, if applicable.
    reference_signal_hz:
        Signal frequency, if applicable.
    metadata:
        Small static dictionary.

    JAX PyTree behavior
    -------------------
    frequencies_hz and indices are dynamic leaves. Labels/roles/kind/metadata
    are static auxiliary data.
    """

    frequencies_hz: jax.Array
    labels: tuple[str, ...]
    roles: tuple[ToneRole, ...]
    indices: jax.Array
    kind: FrequencyPlanKind = FrequencyPlanKind.CUSTOM
    reference_pump_hz: float | None = None
    reference_signal_hz: float | None = None
    metadata: Mapping[str, Any] | None = None

    def __post_init__(self) -> None:
        f = _as_1d_float_array("frequencies_hz", self.frequencies_hz)
        idx = _as_1d_float_array("indices", self.indices).astype(jnp.int32)

        labels = tuple(_unique_labels(tuple(self.labels)))
        roles = tuple(ToneRole(r) for r in self.roles)

        k = int(f.shape[0])
        if len(labels) != k:
            raise ValueError(f"labels length {len(labels)} does not match K={k}")
        if len(roles) != k:
            raise ValueError(f"roles length {len(roles)} does not match K={k}")
        if idx.shape[0] != k:
            raise ValueError(f"indices length {idx.shape[0]} does not match K={k}")

        if bool(jnp.any(jnp.isnan(f))):
            raise ValueError("frequencies_hz contains NaN")
        if bool(jnp.any(jnp.isinf(f))):
            raise ValueError("frequencies_hz contains inf")
        if bool(jnp.any(f == 0.0)):
            zero_roles = [roles[i] for i in range(k) if float(f[i]) == 0.0]
            if any(role != ToneRole.DC for role in zero_roles):
                raise ValueError("Only DC tones may have zero frequency")

        object.__setattr__(self, "frequencies_hz", f)
        object.__setattr__(self, "indices", idx)
        object.__setattr__(self, "labels", labels)
        object.__setattr__(self, "roles", roles)
        object.__setattr__(self, "kind", FrequencyPlanKind(self.kind))
        if self.reference_pump_hz is not None:
            _check_positive("reference_pump_hz", self.reference_pump_hz)
        if self.reference_signal_hz is not None:
            _check_positive("reference_signal_hz", self.reference_signal_hz)
        if self.metadata is None:
            object.__setattr__(self, "metadata", {})
        else:
            object.__setattr__(self, "metadata", dict(self.metadata))

    # ------------------------------------------------------------------
    # JAX PyTree implementation
    # ------------------------------------------------------------------

    def tree_flatten(self) -> tuple[tuple[jax.Array, jax.Array], dict[str, Any]]:
        children = (self.frequencies_hz, self.indices)
        aux = {
            "labels": self.labels,
            "roles": self.roles,
            "kind": self.kind,
            "reference_pump_hz": self.reference_pump_hz,
            "reference_signal_hz": self.reference_signal_hz,
            "metadata": dict(self.metadata or {}),
        }
        return children, aux

    @classmethod
    def tree_unflatten(
        cls,
        aux: dict[str, Any],
        children: tuple[jax.Array, jax.Array],
    ) -> "FrequencyPlan":
        return cls(
            frequencies_hz=children[0],
            labels=aux["labels"],
            roles=aux["roles"],
            indices=children[1],
            kind=aux["kind"],
            reference_pump_hz=aux["reference_pump_hz"],
            reference_signal_hz=aux["reference_signal_hz"],
            metadata=aux["metadata"],
        )

    # ------------------------------------------------------------------
    # Basic properties
    # ------------------------------------------------------------------

    @property
    def n_tones(self) -> int:
        return int(self.frequencies_hz.shape[0])

    @property
    def angular_frequencies_rad_s(self) -> jax.Array:
        return angular_frequency(self.frequencies_hz)

    @property
    def abs_frequencies_hz(self) -> jax.Array:
        return jnp.abs(self.frequencies_hz)

    @property
    def positive_frequency_mask(self) -> jax.Array:
        return self.frequencies_hz > 0.0

    @property
    def negative_frequency_mask(self) -> jax.Array:
        return self.frequencies_hz < 0.0

    @property
    def dc_mask(self) -> jax.Array:
        return self.frequencies_hz == 0.0

    @property
    def sorted_by_frequency(self) -> "FrequencyPlan":
        order = jnp.argsort(self.frequencies_hz)
        labels = tuple(self.labels[int(i)] for i in order)
        roles = tuple(self.roles[int(i)] for i in order)
        return FrequencyPlan(
            frequencies_hz=self.frequencies_hz[order],
            labels=labels,
            roles=roles,
            indices=self.indices[order],
            kind=self.kind,
            reference_pump_hz=self.reference_pump_hz,
            reference_signal_hz=self.reference_signal_hz,
            metadata={**dict(self.metadata or {}), "sorted": "frequency"},
        )

    @property
    def sorted_by_abs_frequency(self) -> "FrequencyPlan":
        order = jnp.argsort(jnp.abs(self.frequencies_hz))
        labels = tuple(self.labels[int(i)] for i in order)
        roles = tuple(self.roles[int(i)] for i in order)
        return FrequencyPlan(
            frequencies_hz=self.frequencies_hz[order],
            labels=labels,
            roles=roles,
            indices=self.indices[order],
            kind=self.kind,
            reference_pump_hz=self.reference_pump_hz,
            reference_signal_hz=self.reference_signal_hz,
            metadata={**dict(self.metadata or {}), "sorted": "abs_frequency"},
        )

    # ------------------------------------------------------------------
    # Lookup helpers
    # ------------------------------------------------------------------

    def role_mask(self, role: ToneRole | str) -> jax.Array:
        role = ToneRole(role)
        return jnp.asarray([r == role for r in self.roles], dtype=bool)

    def labels_for_role(self, role: ToneRole | str) -> list[str]:
        role = ToneRole(role)
        return [label for label, r in zip(self.labels, self.roles) if r == role]

    def positions_for_role(self, role: ToneRole | str) -> list[int]:
        role = ToneRole(role)
        return [i for i, r in enumerate(self.roles) if r == role]

    def position_of_label(self, label: str) -> int:
        try:
            return self.labels.index(label)
        except ValueError as exc:
            raise KeyError(f"Unknown tone label {label!r}") from exc

    def frequency_of_label(self, label: str) -> float:
        return float(self.frequencies_hz[self.position_of_label(label)])

    def angular_frequency_of_label(self, label: str) -> float:
        return float(self.angular_frequencies_rad_s[self.position_of_label(label)])

    def has_label(self, label: str) -> bool:
        return label in self.labels

    def tone(self, label: str) -> Tone:
        pos = self.position_of_label(label)
        return Tone(
            label=self.labels[pos],
            frequency_hz=float(self.frequencies_hz[pos]),
            role=self.roles[pos],
            index=int(self.indices[pos]),
            metadata={"plan_kind": self.kind.value},
        )

    def tones(self) -> list[Tone]:
        return [
            Tone(
                label=self.labels[i],
                frequency_hz=float(self.frequencies_hz[i]),
                role=self.roles[i],
                index=int(self.indices[i]),
                metadata={"plan_kind": self.kind.value},
            )
            for i in range(self.n_tones)
        ]

    def find_frequency(
        self,
        target_hz: float,
        *,
        atol_hz: float = 1e-6,
        rtol: float = 1e-12,
    ) -> list[int]:
        mask = nearly_equal_frequency_mask(
            self.frequencies_hz,
            target_hz,
            atol_hz=atol_hz,
            rtol=rtol,
        )
        return [i for i, value in enumerate(mask.tolist()) if bool(value)]

    def conjugate_position_map(
        self,
        *,
        atol_hz: float = 1e-6,
        rtol: float = 1e-12,
        require_all: bool = False,
    ) -> dict[int, int | None]:
        """
        Map each tone position to the position of its negative-frequency partner.

        If require_all is true, raise if a conjugate is missing.
        """
        mapping: dict[int, int | None] = {}
        for i, f in enumerate(self.frequencies_hz.tolist()):
            matches = self.find_frequency(-float(f), atol_hz=atol_hz, rtol=rtol)
            if len(matches) == 0:
                if require_all:
                    raise ValueError(
                        f"No conjugate frequency found for position {i}, f={f}"
                    )
                mapping[i] = None
            elif len(matches) == 1:
                mapping[i] = matches[0]
            else:
                raise ValueError(
                    f"Multiple conjugate matches found for position {i}, f={f}: {matches}"
                )
        return mapping

    # ------------------------------------------------------------------
    # Conversion / summaries
    # ------------------------------------------------------------------

    def with_updates(self, **kwargs: Any) -> "FrequencyPlan":
        return replace(self, **kwargs)

    def with_metadata(self, **metadata_updates: Any) -> "FrequencyPlan":
        current = dict(self.metadata or {})
        current.update(metadata_updates)
        return replace(self, metadata=current)

    def to_dict(self) -> dict[str, Any]:
        return {
            "kind": self.kind.value,
            "n_tones": self.n_tones,
            "reference_pump_hz": self.reference_pump_hz,
            "reference_signal_hz": self.reference_signal_hz,
            "tones": [tone.to_dict() for tone in self.tones()],
            "metadata": _jsonify(dict(self.metadata or {})),
        }

    def summary_table(self) -> str:
        lines = [
            f"FrequencyPlan(kind={self.kind.value}, K={self.n_tones})",
            "idx | label | role | f_GHz | omega_rad_s",
            "--- | --- | --- | ---: | ---:",
        ]
        for i in range(self.n_tones):
            f = float(self.frequencies_hz[i])
            omega = float(self.angular_frequencies_rad_s[i])
            lines.append(
                f"{i} | {self.labels[i]} | {self.roles[i].value} | "
                f"{f / GHz:.9g} | {omega:.9e}"
            )
        return "\n".join(lines)


