@dataclass(frozen=True)
class PortFrequencyIndex:
    """
    Index mapping for multi-port, multi-frequency wave variables.

    For a two-port plan with K tones, the flattened wave vector can be ordered as

        flat_index = port_index * K + frequency_index

    where port_index is zero-based internally.

    This supports X-parameter-style conversion matrices:

        b[p, omega_j] = sum_{q,k} X[p,j;q,k] a[q,k]
    """

    n_ports: int
    frequency_plan: FrequencyPlan
    port_base: Literal[0, 1] = 1

    def __post_init__(self) -> None:
        if int(self.n_ports) <= 0:
            raise ValueError("n_ports must be positive")
        object.__setattr__(self, "n_ports", int(self.n_ports))
        if self.port_base not in {0, 1}:
            raise ValueError("port_base must be 0 or 1")

    @property
    def n_frequencies(self) -> int:
        return self.frequency_plan.n_tones

    @property
    def size(self) -> int:
        return self.n_ports * self.n_frequencies

    def _port_to_zero_based(self, port: int) -> int:
        p = int(port) - self.port_base
        if p < 0 or p >= self.n_ports:
            raise IndexError(
                f"port {port} out of range for n_ports={self.n_ports}, "
                f"port_base={self.port_base}"
            )
        return p

    def flat_index(self, port: int, frequency_position: int) -> int:
        p = self._port_to_zero_based(port)
        k = int(frequency_position)
        if k < 0 or k >= self.n_frequencies:
            raise IndexError(f"frequency_position {k} out of range")
        return p * self.n_frequencies + k

    def flat_index_label(self, port: int, label: str) -> int:
        return self.flat_index(port, self.frequency_plan.position_of_label(label))

    def unflatten_index(self, flat_index: int) -> tuple[int, int]:
        idx = int(flat_index)
        if idx < 0 or idx >= self.size:
            raise IndexError(f"flat_index {idx} out of range size={self.size}")
        p0 = idx // self.n_frequencies
        k = idx % self.n_frequencies
        return p0 + self.port_base, k

    def matrix_shape(self) -> tuple[int, int]:
        return (self.size, self.size)

    def labels(self) -> list[str]:
        out: list[str] = []
        for p0 in range(self.n_ports):
            p = p0 + self.port_base
            for label in self.frequency_plan.labels:
                out.append(f"p{p}:{label}")
        return out

    def to_dict(self) -> dict[str, Any]:
        return {
            "n_ports": self.n_ports,
            "n_frequencies": self.n_frequencies,
            "size": self.size,
            "port_base": self.port_base,
            "labels": self.labels(),
            "frequency_plan": self.frequency_plan.to_dict(),
        }


