"""
twpa.core.frequency_plan
========================

Frequency bookkeeping for harmonic-balance and conversion-matrix simulation.

The simulator uses explicit frequency plans for three related tasks:

1. Pump-only HB
   Solve the large-signal nonlinear periodic state generated by the pump and
   its harmonics.

2. Small-signal conversion around a pumped solution
   Linearize the circuit equations around the pump-only HB state and solve for
   signal/idler/sideband response.

3. Finite-signal HB
   Later, solve the full nonlinear pump + signal + idler problem for gain
   compression and pump depletion.

Why this exists
---------------
A TWPA is not simply "pump, signal, idler". Realistic harmonic-balance and
X-parameter style simulations use a multi-frequency basis. For degenerate
four-wave mixing, the primary idler satisfies

    f_i = 2 f_p - f_s

and higher sidebands can be written in a signed, double-sided convention as

    omega_k = 2 k omega_p + omega_s

where k = 0 gives the signal and k = -1 gives the negative-frequency idler
component omega_s - 2 omega_p = -omega_i.

The double-sided representation is important for:
- conjugate-symmetry checks,
- nonlinear mixing products,
- X-parameter/sensitivity-matrix indexing,
- later quantum-adapted conversion matrices.

Conventions
-----------
- Frequencies in this file are ordinary frequencies in Hz unless a property or
  function explicitly says angular frequency.
- Angular frequencies are omega = 2 pi f in rad/s.
- Frequency arrays may contain negative entries in double-sided plans.
- Public APIs preserve labels, so downstream code can identify signal, idler,
  pump, harmonics, and auxiliary sidebands.
"""

