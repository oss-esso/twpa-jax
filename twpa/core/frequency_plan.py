def validate_frequency_plan(
    plan: FrequencyPlan,
    *,
    min_abs_frequency_hz: float = 1.0,
    max_abs_frequency_hz: float | None = None,
    require_unique_frequencies: bool = False,
    atol_hz: float = 1e-6,
    rtol: float = 1e-12,
) -> dict[str, Any]:
    """
    Validate a frequency plan and return a report.

    This function does not raise for validation failures except malformed input.
    """
    abs_f = jnp.abs(plan.frequencies_hz)
    nonzero_min = float(jnp.min(abs_f[abs_f > 0.0])) if bool(jnp.any(abs_f > 0.0)) else 0.0

    passed = True
    messages: list[str] = []

    if nonzero_min < min_abs_frequency_hz:
        passed = False
        messages.append(
            f"Minimum nonzero frequency {nonzero_min} Hz is below "
            f"min_abs_frequency_hz={min_abs_frequency_hz}."
        )

    max_abs = float(jnp.max(abs_f))
    if max_abs_frequency_hz is not None and max_abs > max_abs_frequency_hz:
        passed = False
        messages.append(
            f"Maximum frequency {max_abs} Hz exceeds max_abs_frequency_hz="
            f"{max_abs_frequency_hz}."
        )

    duplicate_pairs: list[tuple[int, int]] = []
    for i in range(plan.n_tones):
        for j in range(i + 1, plan.n_tones):
            fi = float(plan.frequencies_hz[i])
            fj = float(plan.frequencies_hz[j])
            if abs(fi - fj) <= (atol_hz + rtol * max(abs(fi), abs(fj))):
                duplicate_pairs.append((i, j))

    if require_unique_frequencies and duplicate_pairs:
        passed = False
        messages.append(f"Duplicate/equal frequencies found: {duplicate_pairs}")

    if not messages:
        messages.append("PASS: frequency plan validation checks passed.")

    return {
        "kind": plan.kind.value,
        "n_tones": plan.n_tones,
        "min_abs_frequency_hz": nonzero_min,
        "max_abs_frequency_hz": max_abs,
        "require_unique_frequencies": require_unique_frequencies,
        "duplicate_pairs": duplicate_pairs,
        "passed": bool(passed),
        "messages": messages,
    }


def validate_dp4wm_plan(
    plan: FrequencyPlan,
    *,
    atol_hz: float = 1e-6,
    rtol: float = 1e-12,
) -> dict[str, Any]:
    """
    Validate DP4WM-specific frequency relationships.

    Checks:
    - reference pump and signal exist,
    - primary idler relation f_i = 2 f_p - f_s is represented by
      a negative-frequency tone at f_s - 2 f_p or a positive idler tone.
    """
    passed = True
    messages: list[str] = []

    if plan.reference_pump_hz is None:
        passed = False
        messages.append("Missing reference_pump_hz.")
    if plan.reference_signal_hz is None:
        passed = False
        messages.append("Missing reference_signal_hz.")

    if plan.reference_pump_hz is not None and plan.reference_signal_hz is not None:
        fp = plan.reference_pump_hz
        fs = plan.reference_signal_hz
        fi = dp4wm_primary_idler_frequency_hz(fp, fs)
        neg_idler = fs - 2.0 * fp

        pos_matches = plan.find_frequency(fi, atol_hz=atol_hz, rtol=rtol)
        neg_matches = plan.find_frequency(neg_idler, atol_hz=atol_hz, rtol=rtol)

        if not pos_matches and not neg_matches:
            passed = False
            messages.append(
                "No primary idler representation found. Expected either "
                f"+fi={fi} Hz or -fi={neg_idler} Hz."
            )
        else:
            messages.append(
                f"Primary idler represented. positive_matches={pos_matches}, "
                f"negative_matches={neg_matches}."
            )

        signal_matches = plan.find_frequency(fs, atol_hz=atol_hz, rtol=rtol)
        if not signal_matches:
            passed = False
            messages.append(f"No signal tone found at fs={fs} Hz.")
        else:
            messages.append(f"Signal tone found at positions {signal_matches}.")

    if passed:
        messages.insert(0, "PASS: DP4WM frequency relationships validated.")
    else:
        messages.insert(0, "FAIL: DP4WM frequency relationship issue.")

    return {
        "kind": plan.kind.value,
        "passed": bool(passed),
        "messages": messages,
        "reference_pump_hz": plan.reference_pump_hz,
        "reference_signal_hz": plan.reference_signal_hz,
    }


