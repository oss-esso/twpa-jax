def make_pump_only_plan(
    pump_frequency_hz: float,
    *,
    n_harmonics: int = 3,
    include_negative: bool = False,
    include_dc: bool = False,
    sort: Literal["none", "frequency", "abs_frequency"] = "abs_frequency",
) -> FrequencyPlan:
    """
    Create a pump-only frequency plan.

    Positive-only example for n_harmonics=3:
        +fp, +2fp, +3fp

    Double-sided example:
        -3fp, -2fp, -fp, +fp, +2fp, +3fp
        with optional DC tone.

    This plan is used for large-signal pump-only HB.
    """
    _check_positive("pump_frequency_hz", pump_frequency_hz)
    if int(n_harmonics) <= 0:
        raise ValueError("n_harmonics must be positive")
    n_harmonics = int(n_harmonics)

    tones: list[Tone] = []

    if include_dc:
        tones.append(Tone(label="dc", frequency_hz=0.0, role=ToneRole.DC, index=0))

    if include_negative:
        for n in range(n_harmonics, 0, -1):
            role = ToneRole.PUMP if n == 1 else ToneRole.PUMP_HARMONIC
            tones.append(
                Tone(
                    label=f"neg_pump_{n}",
                    frequency_hz=-n * pump_frequency_hz,
                    role=role,
                    index=-n,
                    metadata={"harmonic": n},
                )
            )

    for n in range(1, n_harmonics + 1):
        role = ToneRole.PUMP if n == 1 else ToneRole.PUMP_HARMONIC
        label = "pump" if n == 1 else f"pump_{n}"
        tones.append(
            Tone(
                label=label,
                frequency_hz=n * pump_frequency_hz,
                role=role,
                index=n,
                metadata={"harmonic": n},
            )
        )

    plan = _plan_from_tones(
        tones,
        kind=FrequencyPlanKind.PUMP_ONLY,
        reference_pump_hz=pump_frequency_hz,
        reference_signal_hz=None,
        metadata={
            "n_harmonics": n_harmonics,
            "include_negative": include_negative,
            "include_dc": include_dc,
        },
    )

    return _sort_plan(plan, sort)


def make_pump_only_plan_from_config(
    pump: PumpConfig,
    *,
    include_negative: bool = False,
    include_dc: bool = False,
    sort: Literal["none", "frequency", "abs_frequency"] = "abs_frequency",
) -> FrequencyPlan:
    """Create a pump-only plan from PumpConfig."""
    return make_pump_only_plan(
        pump.frequency_hz,
        n_harmonics=pump.harmonics,
        include_negative=include_negative,
        include_dc=include_dc,
        sort=sort,
    )


def make_dp4wm_signal_idler_plan(
    pump_frequency_hz: float,
    signal_frequency_hz: float,
    *,
    sideband_order: int = 3,
    include_pump: bool = True,
    include_pump_harmonics: int = 3,
    include_negative_conjugates: bool = True,
    sort: Literal["none", "frequency", "abs_frequency"] = "frequency",
) -> FrequencyPlan:
    """
    Create a degenerate-pump 4WM small-signal/idler plan.

    Signal/idler sidebands are generated by:

        f_k = f_s + 2 k f_p

    where k is an integer. k=0 is the signal. k=-1 is the negative-frequency
    representation of the primary idler:

        f_{-1} = f_s - 2 f_p = -(2 f_p - f_s) = -f_i

    Parameters
    ----------
    pump_frequency_hz:
        Pump frequency fp.
    signal_frequency_hz:
        Signal frequency fs.
    sideband_order:
        Include k from -sideband_order to +sideband_order.
    include_pump:
        Add +fp as a tone.
    include_pump_harmonics:
        Add pump harmonics +n fp for n=1..include_pump_harmonics if
        include_pump is true. If include_pump is false, this is ignored.
    include_negative_conjugates:
        Ensure signed-opposite partners are included for signal/idler sideband
        tones and pump tones.
    """
    _check_positive("pump_frequency_hz", pump_frequency_hz)
    _check_positive("signal_frequency_hz", signal_frequency_hz)
    if int(sideband_order) < 1:
        raise ValueError("sideband_order must be >= 1")
    sideband_order = int(sideband_order)
    include_pump_harmonics = int(include_pump_harmonics)
    if include_pump_harmonics < 0:
        raise ValueError("include_pump_harmonics must be non-negative")

    tones: list[Tone] = []

    # Signal/idler double-sided sideband set.
    for k in range(-sideband_order, sideband_order + 1):
        f = signal_frequency_hz + 2.0 * k * pump_frequency_hz

        if k == 0:
            role = ToneRole.SIGNAL
            label = "signal"
        elif k == -1:
            role = ToneRole.IDLER
            label = "neg_idler_primary"
        elif k == 1:
            role = ToneRole.SIDEBAND
            label = "upper_sideband_k1"
        elif k < -1:
            role = ToneRole.SIDEBAND
            label = f"lower_sideband_k{k}"
        else:
            role = ToneRole.SIDEBAND
            label = f"upper_sideband_k{k}"

        if f == 0.0:
            raise ValueError(
                "Generated zero-frequency sideband. Choose non-degenerate "
                "pump/signal frequencies or exclude that sideband."
            )

        tones.append(
            Tone(
                label=label,
                frequency_hz=f,
                role=role,
                index=k,
                metadata={
                    "formula": "f_k = f_s + 2 k f_p",
                    "k": k,
                    "pump_frequency_hz": pump_frequency_hz,
                    "signal_frequency_hz": signal_frequency_hz,
                },
            )
        )

    # Optional explicit conjugates of the generated sideband set.
    if include_negative_conjugates:
        tones = _add_missing_conjugate_tones(tones)

    # Optional pump and harmonics.
    if include_pump:
        pump_tones: list[Tone] = []
        for n in range(1, include_pump_harmonics + 1):
            role = ToneRole.PUMP if n == 1 else ToneRole.PUMP_HARMONIC
            label = "pump" if n == 1 else f"pump_{n}"
            pump_tones.append(
                Tone(
                    label=label,
                    frequency_hz=n * pump_frequency_hz,
                    role=role,
                    index=n,
                    metadata={"harmonic": n},
                )
            )
        if include_negative_conjugates:
            pump_tones = _add_missing_conjugate_tones(pump_tones)
        tones.extend(pump_tones)

    # Remove exact duplicate frequencies/labels in a conservative way.
    tones = _deduplicate_tones_by_label_and_frequency(tones)

    plan = _plan_from_tones(
        tones,
        kind=FrequencyPlanKind.DP4WM_SIGNAL_IDLER,
        reference_pump_hz=pump_frequency_hz,
        reference_signal_hz=signal_frequency_hz,
        metadata={
            "sideband_order": sideband_order,
            "include_pump": include_pump,
            "include_pump_harmonics": include_pump_harmonics,
            "include_negative_conjugates": include_negative_conjugates,
            "primary_idler_frequency_hz": 2.0 * pump_frequency_hz - signal_frequency_hz,
        },
    )

    return _sort_plan(plan, sort)


def make_dp4wm_signal_idler_plan_from_config(
    pump: PumpConfig,
    signal_frequency_hz: float,
    *,
    signal: SignalConfig | None = None,
    sideband_order: int | None = None,
    include_pump: bool = True,
    include_pump_harmonics: int | None = None,
    include_negative_conjugates: bool = True,
    sort: Literal["none", "frequency", "abs_frequency"] = "frequency",
) -> FrequencyPlan:
    """Create a DP4WM plan using PumpConfig and optional SignalConfig."""
    if sideband_order is None:
        sideband_order = signal.sideband_order if signal is not None else 3
    if include_pump_harmonics is None:
        include_pump_harmonics = pump.harmonics
    return make_dp4wm_signal_idler_plan(
        pump.frequency_hz,
        signal_frequency_hz,
        sideband_order=sideband_order,
        include_pump=include_pump,
        include_pump_harmonics=include_pump_harmonics,
        include_negative_conjugates=include_negative_conjugates,
        sort=sort,
    )


def make_finite_signal_hb_plan_dp4wm(
    pump_frequency_hz: float,
    signal_frequency_hz: float,
    *,
    pump_harmonics: int = 3,
    sideband_order: int = 2,
    include_negative: bool = True,
    include_dc: bool = False,
    sort: Literal["none", "frequency", "abs_frequency"] = "frequency",
) -> FrequencyPlan:
    """
    Frequency plan for finite-signal HB in DP4WM.

    This combines pump harmonics and signal/idler sidebands into one plan.
    It is intended for compression simulations after the pump-only and
    linearized conversion solvers are validated.
    """
    pump_plan = make_pump_only_plan(
        pump_frequency_hz,
        n_harmonics=pump_harmonics,
        include_negative=include_negative,
        include_dc=include_dc,
        sort="none",
    )
    conversion_plan = make_dp4wm_signal_idler_plan(
        pump_frequency_hz,
        signal_frequency_hz,
        sideband_order=sideband_order,
        include_pump=False,
        include_negative_conjugates=include_negative,
        sort="none",
    )
    tones = pump_plan.tones() + conversion_plan.tones()
    tones = _deduplicate_tones_by_label_and_frequency(tones)

    plan = _plan_from_tones(
        tones,
        kind=FrequencyPlanKind.FINITE_SIGNAL_HB,
        reference_pump_hz=pump_frequency_hz,
        reference_signal_hz=signal_frequency_hz,
        metadata={
            "mixing_regime": MixingRegime.DP4WM.value,
            "pump_harmonics": pump_harmonics,
            "sideband_order": sideband_order,
            "include_negative": include_negative,
            "include_dc": include_dc,
        },
    )
    return _sort_plan(plan, sort)


def make_multi_signal_idler_plan(
    pump_frequency_hz: float,
    signal_frequencies_hz: ArrayLike,
    idler_frequencies_hz: ArrayLike,
    *,
    pump_label: str = "pump",
    signal_labels: Iterable[str] | None = None,
    idler_labels: Iterable[str] | None = None,
    n_pump_harmonics: int = 3,
    include_negative: bool = True,
    include_dc: bool = False,
    sort: Literal["none", "frequency", "abs_frequency"] = "frequency",
) -> FrequencyPlan:
    """Build one target plan for a labelled signal/idler gain sweep."""
    _check_positive("pump_frequency_hz", pump_frequency_hz)
    fs = _as_1d_float_array("signal_frequencies_hz", signal_frequencies_hz)
    fi = _as_1d_float_array("idler_frequencies_hz", idler_frequencies_hz)
    if fs.shape != fi.shape:
        raise ValueError("signal_frequencies_hz and idler_frequencies_hz must have the same shape")
    if bool(jnp.any(fs <= 0.0)) or bool(jnp.any(fi <= 0.0)):
        raise ValueError("signal and idler frequencies must be positive")
    if int(n_pump_harmonics) <= 0:
        raise ValueError("n_pump_harmonics must be positive")

    n_points = int(fs.shape[0])
    sig_labels = tuple(signal_labels or (f"signal_{i}" for i in range(n_points)))
    idl_labels = tuple(idler_labels or (f"idler_{i}" for i in range(n_points)))
    if len(sig_labels) != n_points or len(idl_labels) != n_points:
        raise ValueError("signal_labels and idler_labels must match the frequency array length")

    tones: list[Tone] = []
    if include_dc:
        tones.append(Tone(label="dc", frequency_hz=0.0, role=ToneRole.DC))

    pump_tones = [
        Tone(
            label=pump_label if harmonic == 1 else f"{pump_label}_{harmonic}",
            frequency_hz=harmonic * pump_frequency_hz,
            role=ToneRole.PUMP if harmonic == 1 else ToneRole.PUMP_HARMONIC,
            index=harmonic,
            metadata={"harmonic": harmonic},
        )
        for harmonic in range(1, int(n_pump_harmonics) + 1)
    ]
    tones.extend(_add_missing_conjugate_tones(pump_tones) if include_negative else pump_tones)

    conversion_tones = [
        Tone(label=label, frequency_hz=float(freq), role=ToneRole.SIGNAL, index=index)
        for index, (label, freq) in enumerate(zip(sig_labels, fs))
    ]
    conversion_tones.extend(
        Tone(label=label, frequency_hz=float(freq), role=ToneRole.IDLER, index=index)
        for index, (label, freq) in enumerate(zip(idl_labels, fi))
    )
    tones.extend(
        _add_missing_conjugate_tones(conversion_tones)
        if include_negative
        else conversion_tones
    )

    plan = _plan_from_tones(
        _deduplicate_tones_by_label_and_frequency(tones),
        kind=FrequencyPlanKind.DP4WM_SIGNAL_IDLER,
        reference_pump_hz=pump_frequency_hz,
        reference_signal_hz=float(fs[0]) if n_points else None,
        metadata={
            "n_signal_idler_pairs": n_points,
            "n_pump_harmonics": int(n_pump_harmonics),
            "include_negative": include_negative,
            "include_dc": include_dc,
        },
    )
    return _sort_plan(plan, sort)


def make_custom_plan(
    tones: Iterable[Tone],
    *,
    kind: FrequencyPlanKind | str = FrequencyPlanKind.CUSTOM,
    reference_pump_hz: float | None = None,
    reference_signal_hz: float | None = None,
    metadata: Mapping[str, Any] | None = None,
    sort: Literal["none", "frequency", "abs_frequency"] = "none",
) -> FrequencyPlan:
    """Create a custom frequency plan from explicit Tone objects."""
    plan = _plan_from_tones(
        list(tones),
        kind=kind,
        reference_pump_hz=reference_pump_hz,
        reference_signal_hz=reference_signal_hz,
        metadata=metadata,
    )
    return _sort_plan(plan, sort)


# ---------------------------------------------------------------------------
# Plan internals
# ---------------------------------------------------------------------------

def _plan_from_tones(
    tones: list[Tone],
    *,
    kind: FrequencyPlanKind | str,
    reference_pump_hz: float | None,
    reference_signal_hz: float | None,
    metadata: Mapping[str, Any] | None = None,
) -> FrequencyPlan:
    if len(tones) == 0:
        raise ValueError("Cannot build a FrequencyPlan with zero tones")

    labels = tuple(t.label for t in tones)
    _unique_labels(labels)

    frequencies = jnp.asarray([float(t.frequency_hz) for t in tones], dtype=jnp.float64)
    roles = tuple(t.role for t in tones)
    indices = jnp.asarray([int(t.index) for t in tones], dtype=jnp.int32)

    return FrequencyPlan(
        frequencies_hz=frequencies,
        labels=labels,
        roles=roles,
        indices=indices,
        kind=FrequencyPlanKind(kind),
        reference_pump_hz=reference_pump_hz,
        reference_signal_hz=reference_signal_hz,
        metadata=dict(metadata or {}),
    )


def _sort_plan(
    plan: FrequencyPlan,
    sort: Literal["none", "frequency", "abs_frequency"],
) -> FrequencyPlan:
    if sort == "none":
        return plan
    if sort == "frequency":
        return plan.sorted_by_frequency
    if sort == "abs_frequency":
        return plan.sorted_by_abs_frequency
    raise ValueError(f"Unsupported sort mode {sort!r}")


def _add_missing_conjugate_tones(
    tones: list[Tone],
    *,
    atol_hz: float = 1e-6,
    rtol: float = 1e-12,
) -> list[Tone]:
    """
    Add signed-opposite tones when missing.

    Labels are generated from Tone.conjugate_label(), with suffixes added if
    required to avoid collisions.
    """
    out = list(tones)
    freqs = [float(t.frequency_hz) for t in out]
    labels = {t.label for t in out}

    for tone in list(tones):
        target = -float(tone.frequency_hz)
        exists = any(abs(f - target) <= (atol_hz + rtol * abs(target)) for f in freqs)
        if exists:
            continue

        conj = tone.conjugate()
        label = conj.label
        if label in labels:
            base = label
            counter = 1
            while label in labels:
                label = f"{base}_{counter}"
                counter += 1
            conj = replace(conj, label=label)

        out.append(conj)
        freqs.append(float(conj.frequency_hz))
        labels.add(conj.label)

    return out


def _deduplicate_tones_by_label_and_frequency(
    tones: list[Tone],
    *,
    atol_hz: float = 1e-6,
    rtol: float = 1e-12,
) -> list[Tone]:
    """
    Conservative deduplication.

    If labels duplicate, rename later labels. If frequencies duplicate exactly
    within tolerance, keep both only if roles differ in a way that matters.
    For the first implementation, duplicate frequencies with different labels
    are allowed because pump harmonics can coincide with generated sidebands in
    some artificial test cases; downstream code can decide how to handle them.
    """
    out: list[Tone] = []
    used_labels: set[str] = set()

    for tone in tones:
        label = tone.label
        if label in used_labels:
            base = label
            counter = 1
            while label in used_labels:
                label = f"{base}_{counter}"
                counter += 1
            tone = replace(tone, label=label)
        out.append(tone)
        used_labels.add(tone.label)

    return out


